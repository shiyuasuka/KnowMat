        "   (e.g., a table with 'Parallel-BD' and 'Perpendicular-BD' rows), create SEPARATE\n"
        "   composition entries for each direction and fill this field.\n"
        "   Use null if direction is not relevant.\n\n"
        "10. **xrd_details**: XRD instrument info and phase identification results ONLY.\n"
        "    Example: 'XRD (D8 Advance, Cu-Ka, 40kV/40mA, 20-100°, 1.5°/min); single BCC phase'\n"
        "    Do NOT include grain morphology or EBSD results here. Use null if not mentioned.\n\n"
        "11. **microstructure_description**: Microstructure morphology from SEM/EBSD/TEM.\n"
        "    Include grain shape, distribution, texture, columnar vs equiaxed, precipitate morphology.\n"
        "    Example: 'Columnar grains on XZ plane, equiaxed on XY plane; sigma phase at grain boundaries'\n"
        "    Do NOT include XRD instrument parameters here. Use null if not described.\n\n"
        "12. **measurement_condition** for properties: ALWAYS start with test temperature in Kelvin.\n"
        "   Format: 'at XXX K; [other conditions]'.\n"
        "   If paper gives °C, convert to K (°C + 273).\n"
        "   Room temperature = 298 K.\n"
        "   Examples:\n"
        "     'at 298 K; strain rate 1e-3 /s'\n"
        "     'at 873 K; heating rate 20 K/min; Ar atmosphere'\n"
        "     'at 1073 K; compression test; Ø 5mm × 10mm sample'\n\n"
