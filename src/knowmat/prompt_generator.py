        "═══════════════════════════════════════════════════════════════════════════\n"
        "If the SAME composition is tested under DIFFERENT conditions, create SEPARATE composition entries.\n"
        "Examples of different conditions:\n"
        "  - As-built vs. heat-treated samples\n"
        "  - Horizontal vs. vertical loading directions (relative to build direction)\n"
        "  - Different processing parameters (laser power, scan speed, etc.)\n"
        "  - Different heat treatment schedules\n\n"
        "Append the condition to the composition name, e.g.:\n"
        "  'Ti42Hf21Nb21V16 [As-Built]' vs 'Ti42Hf21Nb21V16 [Annealed 973K/64h]'\n"
        "  'Nb15Ta10W75 [Vertical BD]' vs 'Nb15Ta10W75 [Horizontal BD]'\n"
        "  'Co42Cr20Ni30Ti4Al4 [Stress-Relieved]' vs 'Co42Cr20Ni30Ti4Al4 [973K-64h-Aged]'\n"
        "═══════════════════════════════════════════════════════════════════════════\n\n"
        "EXTRACTION INSTRUCTIONS FOR COMPOSITION FIELDS:\n"
        "═══════════════════════════════════════════════════════════════════════════\n"
        "For each composition entry, extract the following fields:\n\n"
        "1. **source_doi**: Extract the paper's DOI from anywhere in the text (title area, header, footer, abstract).\n"
        "   Format: '10.XXXX/...' (e.g., '10.1016/j.msea.2024.147225'). Use null if not found.\n"
        "   Look for patterns like 'https://doi.org/10.xxxx/...' or '10.xxxx/...' near the title or page header.\n\n"
        "2. **composition_normalized**: Rewrite composition using ONLY element symbols and atomic percentages.\n"
        "   Remove ALL abbreviations, parenthetical codes, and sample names.\n"
        "   Examples:\n"
        "     'Ti42Hf21Nb21V16 (T42)' → 'Ti42Hf21Nb21V16'\n"
        "     'FeCoCrNiMo0.5 (FCCN-M)' → 'FeCoCrNiMo0.5'\n"
        "     '(Nb15Ta10W75)98.5C1.5' → 'Nb14.775Ta9.85W73.875C1.5' (normalize nested)\n\n"
        "3. **main_phase**: Identify the primary crystal structure from XRD, EBSD, or electron diffraction data.\n"
        "   Use standard abbreviations: BCC, FCC, HCP, amorphous, or combinations.\n"
        "   Examples: 'BCC', 'FCC + L12', 'HCP', 'amorphous', 'BCC + Laves', 'BCC + sigma'.\n"
        "   Use null if not mentioned.\n\n"
        "4. **has_precipitates**: Set to true if ANY secondary phases or precipitates are mentioned:\n"
        "   sigma, mu, L12, L21, carbides (NbC, TiC), nitrides, oxides, Laves phase, etc.\n"
        "   Set to false if paper explicitly states single-phase or no precipitates.\n\n"
        "5. **grain_size_avg_um**: Extract average grain size in micrometers (μm).\n"
        "   If reported in nm, convert to μm (divide by 1000).\n"
        "   Examples: '50 nm' → 0.05, '120 μm' → 120.0, '1-5 μm' → 3.0 (use midpoint).\n"
        "   IMPORTANT: Look carefully for grain size data in EBSD results, SEM images, and tables.\n"
        "   If different planes have different grain sizes, use the average or the primary plane value.\n"
        "   Use null if not reported.\n\n"
        "6. **grain_size_text**: Quote the original text describing grain size measurements.\n"
        "   Include measurement method and direction-dependent values if present.\n"
        "   Example: 'XY plane equiaxed ~200 um; XZ plane columnar 10.4/6.72 um (line intercept)'\n"
        "   Use null if not reported.\n\n"
        "7. **process_category**: Classify the manufacturing process into ONE category:\n"
        "   - AM_DED: Directed Energy Deposition, LENS, laser cladding\n"
        "   - AM_LPBF: Laser Powder Bed Fusion (general term)\n"
        "   - AM_SLM: Selective Laser Melting (specific subtype of LPBF)\n"
        "   - SPS: Spark Plasma Sintering\n"
        "   - Arc_Melting: Arc melting, vacuum arc melting\n"
        "   - HeatTreat: Heat treatment, annealing, aging (post-processing only, not primary synthesis)\n"
        "   - Casting: Casting, melt spinning, suction casting\n"
        "   - Unknown: If process is unclear or not mentioned\n\n"
        "8. **processing_params**: Extract structured key process parameters as a JSON object.\n"
        "   Use ONLY these standardised keys (omit any that are not explicitly stated):\n"
        "     Laser_Power_W, Scan_Speed_mm_s, Layer_Thickness_um, Hatch_Spacing_um,\n"
        "     Preheat_Temperature_C, Shielding_Gas, Oxygen_Content_ppm, Build_Orientation.\n"
        "   Example from text 'P = 300 W, v = 400 mm/s, t = 40 μm, h = 30 μm, argon (O₂ < 100 ppm)':\n"
        "     {\"Laser_Power_W\": 300, \"Scan_Speed_mm_s\": 400, \"Layer_Thickness_um\": 40,\n"
        "      \"Hatch_Spacing_um\": 30, \"Shielding_Gas\": \"Ar\", \"Oxygen_Content_ppm\": 100}\n"
        "   Use null if no structured parameters can be extracted.\n\n"
        "9. **build_orientation**: If this entry represents a specific build/loading direction,\n"
