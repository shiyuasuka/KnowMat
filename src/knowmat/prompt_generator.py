        "      \"build_orientation\": \"string or null (Parallel-BD/Perpendicular-BD/etc.)\",\n"
        "      \"xrd_details\": \"string or null (XRD instrument + phase ID only)\",\n"
        "      \"microstructure_description\": \"string or null (grain morphology from SEM/EBSD/TEM)\",\n"
